package org.request;

public class RequestList {
	
	private RequestValue authorised;
	private RequestValue unAuthorised;
	public RequestValue getAuthorised() {
		return authorised;
	}
	public void setAuthorised(RequestValue authorised) {
		this.authorised = authorised;
	}
	public RequestValue getUnAuthorised() {
		return unAuthorised;
	}
	public void setUnAuthorised(RequestValue unAuthorised) {
		this.unAuthorised = unAuthorised;
	}
	

}
